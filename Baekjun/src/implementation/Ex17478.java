package implementation;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Ex17478 {
	public static int n;
	public static StringBuilder sb = new StringBuilder();

	public static void main(String[] args) throws IOException{
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		
		n = Integer.parseInt(br.readLine());
		
		sb.append("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.").append("\n");
		recursion(0);
		
		System.out.println(sb);
	}
	
	public static void recursion(int a) {
		
		if(a<n) {
			for(int i=0; i<4; i++) {
				for(int j=0; j<a; j++) {
					spec();
				}
				shout(i);
			}
		} else if(a==n) {
			for(int i=0; i<6; i++) {
				if(i==1 || i==2 || i==3) continue;
				for(int j=0; j<a; j++) {
					spec();
				}
				shout(i);
			}
			return;
		}
		
		recursion(a+1);
		
		for(int i=0; i<a; i++) {
			spec();
		}
		shout(5);
		
		
	}

	public static void shout(int n) {
		if(n==0) {
			sb.append("\"재귀함수가 뭔가요?\"").append("\n");
		} else if(n==1) {
			sb.append("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.").append("\n");
		} else if(n==2) {
			sb.append("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.").append("\n");
		} else if(n==3){
			sb.append("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"").append("\n");
		} else if(n==4){
			sb.append("\"재귀함수는 자기 자신을 호출하는 함수라네\"").append("\n");
		} else {
			sb.append("라고 답변하였지.").append("\n");
		}
	}
	public static void spec() {
		sb.append("____");
	}

}
